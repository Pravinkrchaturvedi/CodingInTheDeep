package ch3_stacks_queues;

public class Node implements Comparable<Object>{
   Object value;
   Node next;
   
   public Node(){
      this.value = null;
      next = null;
   }
   public Node(Object value){
      this.value = value;
      next = null;
   }
   
   public int compareTo(Object other_obj) {
      return compareTo(other_obj);
   }
   
   
}
